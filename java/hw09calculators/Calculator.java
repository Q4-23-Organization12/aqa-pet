package hw09calculators;

public class Calculator {

    double add(double x, double y) {
        return x + y;
    }

    double subtract(double x, double y) {
        return x - y;
    }

    double multiply(double x, double y) {
        return x * y;
    }

    public double divide(double x, double y) { return x / y; }
}
